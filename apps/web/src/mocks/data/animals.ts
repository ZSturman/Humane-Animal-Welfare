// Helper to get at-risk animals (CRITICAL, HIGH, ELEVATED)
export const getAtRiskAnimals = (): MockAnimal[] => {
  return mockAnimals.filter(
    (animal) =>
      animal.riskProfile &&
      ['CRITICAL', 'HIGH', 'ELEVATED'].includes(animal.riskProfile.severity)
  );
};

// Helper to get animals by status
export const getAnimalsByStatus = (status: string): MockAnimal[] => {
  return mockAnimals.filter((animal) => animal.status === status);
};

// Helper to search animals
export const searchAnimals = (query: string): MockAnimal[] => {
  const lowerQuery = query.toLowerCase();
  return mockAnimals.filter(
    (animal) =>
      animal.name.toLowerCase().includes(lowerQuery) ||
      animal.breed?.toLowerCase().includes(lowerQuery) ||
      animal.species.toLowerCase().includes(lowerQuery)
  );
};

export default mockAnimals;
