// Local type definitions for mock data
type RiskSeverity = 'CRITICAL' | 'HIGH' | 'MODERATE' | 'LOW';
type AnimalStatus = 'AVAILABLE' | 'ADOPTED' | 'HOLD' | 'MEDICAL' | 'BEHAVIORAL' | 'TRANSFER' | 'DECEASED';
