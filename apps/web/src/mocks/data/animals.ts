// Helper to generate consistent placeholder images
const getAnimalImage = (id: number, species: string): string => {
  // Use different image ranges for different species for variety
  return `https://picsum.photos/seed/${species.toLowerCase()}${id}/400/400`;
};

// Helper to generate dates
const daysAgo = (days: number): string => {
  const date = new Date();
  date.setDate(date.getDate() - days);
  return date.toISOString();
};

