export interface MockRiskProfile {
  id: string;
  animalId: string;
  urgencyScore: number;
  severity: 'CRITICAL' | 'HIGH' | 'ELEVATED' | 'MODERATE' | 'LOW';
  riskReasons: string[];
  losScore: number;
  medicalScore: number;
  behavioralScore: number;
  capacityScore: number;
  seniorScore: number;
  specialNeedsScore: number;
  lastCalculatedAt: string;
  manualOverride: boolean;
  overrideReason: string | null;
}

const createRiskProfile = (
  animalId: string,
  urgencyScore: number,
  reasons: string[]
): MockRiskProfile => {
  let severity: MockRiskProfile['severity'];
  if (urgencyScore >= 80) severity = 'CRITICAL';
  else if (urgencyScore >= 60) severity = 'HIGH';
  else if (urgencyScore >= 40) severity = 'ELEVATED';
  else if (urgencyScore >= 20) severity = 'MODERATE';
  else severity = 'LOW';

  return {
    id: `risk-${animalId}`,
    animalId,
    urgencyScore,
    severity,
    riskReasons: reasons,
    losScore: Math.floor(Math.random() * 25),
    medicalScore: Math.floor(Math.random() * 20),
    behavioralScore: Math.floor(Math.random() * 15),
    capacityScore: Math.floor(Math.random() * 15),
    seniorScore: reasons.includes('SENIOR') ? 10 : 0,
    specialNeedsScore: reasons.includes('SPECIAL_NEEDS') ? 10 : 0,
    lastCalculatedAt: new Date().toISOString(),
    manualOverride: false,
    overrideReason: null,
  };
};

